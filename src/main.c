typedef struct {
    mem_t mem; cpu_t cpu; timer_t timer; joypad_t joypad; ppu_t ppu; apu_t apu;
    SDL_Window *window; SDL_Renderer *renderer; SDL_Texture *texture;
    SDL_Tray *tray; SDL_AudioStream *audio_stream;
    bool rom_loaded, quit, paused;
    int menu_selection;
    u32 frame_count, fps_timer;
    s16 audio_buf[2048];
} gb_t;

static void draw_char(u32 *fb, int x, int y, char c, u32 color) {
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7F) c = '.';
    int idx = (c - 0x20) * 8;
    for (int row = 0; row < 8 && y + row < 144; row++) {
        u8 bits = fontdata[idx + row];
        for (int col = 0; col < 8 && x + col < 160; col++)
            if (bits & (0x80 >> col)) fb[(y + row) * 160 + (x + col)] = color;
    }
}
static void draw_text(u32 *fb, int x, int y, const char *text, u32 color) {
    while (*text) { draw_char(fb, x, y, *text, color); x += 8; text++; }
}

static void reset_emu(gb_t *gb) {
    mem_init(&gb->mem); cpu_init(&gb->cpu); timer_init(&gb->timer);
    joypad_init(&gb->joypad); ppu_init(&gb->ppu); apu_init(&gb->apu);
    gb->paused = false;
}

static void load_rom(gb_t *gb, const char *path) {
    if (!mem_load_rom(&gb->mem, path)) return;
    reset_emu(gb);
    gb->mem.joypad = &gb->joypad;
    if (!mem_load_rom(&gb->mem, path)) return;
    gb->rom_loaded = true;
    if (gb->audio_stream) { SDL_DestroyAudioStream(gb->audio_stream); gb->audio_stream = NULL; }
    SDL_AudioSpec spec = {0};
    spec.format = SDL_AUDIO_S16; spec.channels = 2; spec.freq = SAMPLE_RATE;
    gb->audio_stream = SDL_OpenAudioDeviceStream(SDL_AUDIO_DEVICE_DEFAULT_PLAYBACK, &spec, NULL, NULL);
    if (gb->audio_stream) SDL_ResumeAudioDevice(SDL_GetAudioStreamDevice(gb->audio_stream));
    gb->apu.stream = gb->audio_stream;
}

static void rom_cb(void *userdata, const char * const *filelist, int fi) {
    (void)fi; if (filelist && filelist[0]) load_rom((gb_t *)userdata, filelist[0]);
}
static void tr_open(void *userdata, SDL_TrayEntry *e) {
    (void)e; gb_t *gb = (gb_t *)userdata;
    SDL_DialogFileFilter filter = {"Game Boy ROMs", "gb;gbc"};
    SDL_ShowOpenFileDialog(rom_cb, gb, gb->window, &filter, 1, NULL, false);
}
static void tr_quit(void *userdata, SDL_TrayEntry *e) {
    (void)e; ((gb_t *)userdata)->quit = true;
}

static void draw_menu(gb_t *gb) {
    u32 *fb = gb->ppu.framebuffer;
    for (int y = 0; y < 144; y++)
        for (int x = 0; x < 160; x++) {
            u32 c = fb[y * 160 + x];
            fb[y * 160 + x] = 0xFF000000 | ((u32)(((c>>16)&0xFF)*0.5f))<<16 | ((u32)(((c>>8)&0xFF)*0.5f))<<8 | (u32)((c&0xFF)*0.5f);
        }
    for (int y = 40; y < 110; y++) for (int x = 40; x < 120; x++) fb[y*160+x] = 0xFF444444;
    const char *items[] = {"Resume", "Open ROM", "Quit"};
    for (int i = 0; i < 3; i++) {
        u32 col = (i == gb->menu_selection) ? 0xFFFFFFFF : 0xFFCCCCCC;
        if (i == gb->menu_selection) draw_text(fb, 44, 50 + i*20, ">", 0xFFFFFFFF);
        draw_text(fb, 54, 50 + i*20, items[i], col);
    }
}

int main(int argc, char **argv) {
    gb_t gb = {0};
    cpu_init_opcodes();
    reset_emu(&gb);
    gb.mem.joypad = &gb.joypad;

    if (!SDL_Init(SDL_INIT_VIDEO | SDL_INIT_AUDIO)) return 1;
    gb.window = SDL_CreateWindow("PurpleGB", 480, 432, SDL_WINDOW_RESIZABLE);
    if (!gb.window) { SDL_Quit(); return 1; }
    gb.renderer = SDL_CreateRenderer(gb.window, NULL);
    if (!gb.renderer) { SDL_DestroyWindow(gb.window); SDL_Quit(); return 1; }
    gb.texture = SDL_CreateTexture(gb.renderer, SDL_PIXELFORMAT_ARGB8888,
                                    SDL_TEXTUREACCESS_STREAMING, 160, 144);
    if (!gb.texture) { SDL_DestroyRenderer(gb.renderer); SDL_DestroyWindow(gb.window); SDL_Quit(); return 1; }

    SDL_Surface *ts = SDL_CreateSurface(16, 16, SDL_PIXELFORMAT_INDEX8);
    if (ts) {
        SDL_FillSurfaceRect(ts, NULL, 0xFF00AAFF);
        gb.tray = SDL_CreateTray(ts, "PurpleGB");
        if (gb.tray) {
            SDL_TrayMenu *m = SDL_CreateTrayMenu(gb.tray);
            SDL_TrayEntry *oe = SDL_InsertTrayEntryAt(m, -1, "Open ROM...", SDL_TRAYENTRY_BUTTON);
            SDL_SetTrayEntryCallback(oe, tr_open, &gb);
            SDL_InsertTrayEntryAt(m, -1, NULL, 0);
            SDL_TrayEntry *qe = SDL_InsertTrayEntryAt(m, -1, "Quit", SDL_TRAYENTRY_BUTTON);
            SDL_SetTrayEntryCallback(qe, tr_quit, &gb);
        }
        SDL_DestroySurface(ts);
    }

    if (argc > 1) load_rom(&gb, argv[1]);
    else {
        SDL_DialogFileFilter filter = {"Game Boy ROMs", "gb;gbc"};
        SDL_ShowOpenFileDialog(rom_cb, &gb, gb.window, &filter, 1, NULL, false);
    }

    gb.fps_timer = SDL_GetTicks();
    while (!gb.quit) {
        u32 frame_start = SDL_GetTicks();
        SDL_Event e;
        while (SDL_PollEvent(&e)) {
            switch (e.type) {
                case SDL_EVENT_QUIT: gb.quit = true; break;
                case SDL_EVENT_DROP_FILE: if (e.drop.data) load_rom(&gb, e.drop.data); break;
                case SDL_EVENT_KEY_DOWN:
                    if (e.key.key == SDLK_ESCAPE && gb.rom_loaded) { gb.paused = !gb.paused; gb.menu_selection = 0; }
                    if (gb.paused) {
                        if (e.key.key == SDLK_UP) gb.menu_selection = (gb.menu_selection + 2) % 3;
                        if (e.key.key == SDLK_DOWN) gb.menu_selection = (gb.menu_selection + 1) % 3;
                        if (e.key.key == SDLK_RETURN) {
                            gb.paused = false;
                            if (gb.menu_selection == 1) {
                                SDL_DialogFileFilter filter = {"Game Boy ROMs", "gb;gbc"};
                                SDL_ShowOpenFileDialog(rom_cb, &gb, gb.window, &filter, 1, NULL, false);
                            } else if (gb.menu_selection == 2) gb.quit = true;
                        }
                    }
                    joypad_handle_event(&gb.joypad, &gb.mem, &e);
                    break;
                case SDL_EVENT_KEY_UP: joypad_handle_event(&gb.joypad, &gb.mem, &e); break;
                default: break;
            }
        }

        if (!gb.paused && gb.rom_loaded) {
            int total = 0;
            while (total < 70224) {
                int cyc = cpu_step(&gb.cpu, &gb.mem);
                ppu_tick(&gb.ppu, &gb.mem, cyc);
                timer_tick(&gb.timer, &gb.mem, cyc);
                apu_tick(&gb.apu, cyc);
                mem_dma_tick(&gb.mem, cyc);
                total += cyc;
            }
            int samples = (int)(((u64)70224 * SAMPLE_RATE) / T_CYCLES_PER_SEC);
            if (samples > 512) samples = 512;
            apu_generate(&gb.apu, gb.audio_buf, samples);
            if (gb.audio_stream)
                SDL_PutAudioStreamData(gb.audio_stream, gb.audio_buf, samples * sizeof(s16) * 2);
        }

        gb.frame_count++;
        u32 now = SDL_GetTicks();
        if (now - gb.fps_timer >= 1000) {
            char title[128];
            snprintf(title, sizeof(title), "PurpleGB — %s [%u FPS]",
                     gb.rom_loaded ? gb.mem.rom_title : "(no ROM)", gb.frame_count);
            SDL_SetWindowTitle(gb.window, title);
            gb.fps_timer = now; gb.frame_count = 0;
        }
        // TEMP: force LCDC on after 60 frames
        if (gb.rom_loaded && gb.frame_count == 60) {
            gb.mem.io[0x40] = 0x91;
            fprintf(stderr, "FORCED LCDC=0x91 at frame 60\n");
        }
        if (gb.paused && gb.rom_loaded) draw_menu(&gb);
        u32 elapsed = SDL_GetTicks() - frame_start;
        if (elapsed < 16) SDL_Delay(16 - elapsed);
    }

    if (gb.audio_stream) SDL_DestroyAudioStream(gb.audio_stream);
    if (gb.tray) SDL_DestroyTray(gb.tray);
    SDL_DestroyTexture(gb.texture); SDL_DestroyRenderer(gb.renderer); SDL_DestroyWindow(gb.window);
    SDL_Quit();
    free(gb.mem.rom); free(gb.mem.eram);
    return 0;
}
